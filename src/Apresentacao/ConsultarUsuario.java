    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
 if (jTUsuarios.getSelectedRow() != -1) {

            UsuarioFun uf = new UsuarioFun();
            UsuarioFunDAO dao = new UsuarioFunDAO();
            
            uf.setId((int) jTUsuarios.getValueAt(jTUsuarios.getSelectedRow(), 0));           
            uf.setLoginFun(txtLog.getText());           
            uf.setSenhaFun(txtPass.getText());           
            uf.setNomeFun(txtNomeFun.getText());
           
            
            
            dao.updateUsuario(uf);

            txtLog.setText("");
            txtNomeFun.setText("");
            txtPass.setText("");

            readJTable();

        }        
    }//GEN-LAST:event_jButton3ActionPerformed

