   public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) jTUsuarios.getModel();
        modelo.setNumRows(0);
        UsuarioFunDAO pdao = new UsuarioFunDAO();

        for (UsuarioFun p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getLoginFun(),
                p.getSenhaFun(),
            });

        }

    }

  
    
       public void readJTableForLogin(String login) {
        
        DefaultTableModel modelo = (DefaultTableModel) jTUsuarios.getModel();
        modelo.setNumRows(0);
        UsuarioFunDAO pdao = new UsuarioFunDAO();

        for (UsuarioFun p : pdao.readForUsuario(login)) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getLoginFun(),
                p.getSenhaFun(),
                p.getNomeFun()
            });

        }

    }
