    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
if (jTCliente.getSelectedRow() != -1) {

            Pessoa p = new Pessoa();
      
            PessoaDAO dao = new PessoaDAO();

            p.setId((int) jTCliente.getValueAt(jTCliente.getSelectedRow(), 0));
            
            dao.deleteCliente(p);

            
            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarActionPerformed
         Pessoa p1= new Pessoa();
        PessoaDAO dao = new PessoaDAO();  
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd/MM/yyyy");
        String dataFormatada = simpleDateFormat.format(this.jDateChooser1.getDate());
       
       p1.setNome(txtNome.getText());
        p1.setTelefone(txtTelefone.getText());
        p1.setNascData(dataFormatada);
        
        
        dao.criarCliente(p1);
        
        txtNome.setText("");
        txtTelefone.setText("");
        jDateChooser1.setDateFormatString("");        
        
        
        
    }//GEN-LAST:event_jBCadastrarActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
         if (jTCliente.getSelectedRow() != -1) {
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd/MM/yyyy");
        String dataFormatada = simpleDateFormat.format(this.jDateChooser1.getDate());
            
            Pessoa p = new Pessoa();
            PessoaDAO dao = new PessoaDAO();
            
            p.setId((int) jTCliente.getValueAt(jTCliente.getSelectedRow(), 0));           
            p.setNome(txtNome.getText());           
            p.setTelefone(txtTelefone.getText());           
            p.setNascData(dataFormatada);
           
            
            
            dao.updateCliente(p);

            txtNome.setText("");
            txtTelefone.setText("");
            jDateChooser1.setDateFormatString(""); 

            readJTable();

        }
    }//GEN-LAST:event_jButton4ActionPerformed

