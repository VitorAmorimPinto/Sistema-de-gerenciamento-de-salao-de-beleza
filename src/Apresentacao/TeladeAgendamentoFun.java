    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      
        Pessoa p = new Pessoa();
        Agendamento ag = new Agendamento();
        AgendamentoDAO agd = new AgendamentoDAO();
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd/MM/yyyy");
        String dataFormatada = simpleDateFormat.format(this.jDateChooser2.getDate());
        
        ag.setHorario(Double.parseDouble(txtHorario.getText()));
        ag.setProcedimento(txtProce.getText());
        ag.setPreco(Double.parseDouble(txtPreco.getText()));
        ag.setData(dataFormatada);       
        System.out.println(txtID);
        ag.setNome(txtID);
        System.out.println("Imprimir "+ag.getNome()+txtID);
        agd.Agendar(ag);
        
        txtNomeCliente.setText("");
        txtHorario.setText("");
        txtPreco.setText("");
        txtProce.setText("");
        jDateChooser2.setDateFormatString("");
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

