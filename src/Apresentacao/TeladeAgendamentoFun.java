 public void readJTableForNome(String nome) {
        
        DefaultTableModel modelo = (DefaultTableModel) jTAgendamento.getModel();
        modelo.setNumRows(0);
        PessoaDAO pdao = new PessoaDAO();

        for (Pessoa p : pdao.readForNome(nome)) {

            modelo.addRow(new Object[]{
               
                p.getNome(),
                p.getId()
               
               
            });

        }

    }
