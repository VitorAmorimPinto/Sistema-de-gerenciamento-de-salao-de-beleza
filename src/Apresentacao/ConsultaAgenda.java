    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       Agendamento ag = new Agendamento();
       AgendamentoDAO agd = new AgendamentoDAO();   
         if (jTAgenda.getSelectedRow() != -1) {
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd/MM/yyyy");
        String dataFormatada = simpleDateFormat.format(this.jDateChooser1.getDate());
                      
            
            ag.setId((int) jTAgenda.getValueAt(jTAgenda.getSelectedRow(), 0));           
            ag.setPreco(Double.parseDouble(txtPreco.getText()));           
            ag.setProcedimento(txtProced.getText());           
            ag.setData(dataFormatada);
            ag.setHorario(Double.parseDouble(txtHora.getText()));
           
            
            
            agd.updateAgenda(ag);

            txtHora.setText("");
            txtPreco.setText("");
            jDateChooser1.setDateFormatString(""); 
            txtProced.setText("");

            LerJTable();

        }
    }//GEN-LAST:event_jButton3ActionPerformed

