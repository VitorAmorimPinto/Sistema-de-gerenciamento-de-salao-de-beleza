    public void LerJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTAgenda.getModel();
        modelo.setNumRows(0);
       AgendamentoDAO agdao = new AgendamentoDAO();

        for (Agendamento ag : agdao.Ler()) {

            modelo.addRow(new Object[]{
                ag.getId(),
                ag.getNome(),
                ag.getProcedimento(),
                ag.getPreco(),
                ag.getData(),
                ag.getHorario()
            });

        }

    }

     public void BuscaNaTabelaAgendamento(String agendamento) {
        
        DefaultTableModel modelo = (DefaultTableModel) jTAgenda.getModel();
        modelo.setNumRows(0);
        AgendamentoDAO agdao = new AgendamentoDAO();

        for(Agendamento ag : agdao.BuscaPorAgendamento(agendamento)) {

            modelo.addRow(new Object[]{
                ag.getId(),
                ag.getNome(),
                ag.getProcedimento(),
                ag.getPreco(),
                ag.getData(),
                ag.getHorario(),
            });

        }

    }
   
